import sqlite3
import pandas as pd

conn = sqlite3.connect("olist_ecommerce.db")
df = pd.read_sql_query(
    """
    SELECT 
    o.order_id,
    o.customer_id,
    oi.seller_id,
    p.product_category_name,
    r.review_score,
    r.review_comment_message,  -- Crucial for sentiment analysis
    o.order_purchase_timestamp,
    o.order_delivered_customer_date,
    o.order_estimated_delivery_date,
    -- Calculate delay in days
    JULIANDAY(o.order_delivered_customer_date) - JULIANDAY(o.order_estimated_delivery_date) AS delivery_delay_days
FROM olist_orders_dataset o
JOIN olist_order_reviews_dataset r ON o.order_id = r.order_id
JOIN olist_order_items_dataset oi ON o.order_id = oi.order_id
LEFT JOIN olist_products_dataset p ON oi.product_id = p.product_id
WHERE o.order_status = 'delivered'
AND r.review_score = 5
AND (JULIANDAY(o.order_delivered_customer_date) - JULIANDAY(o.order_estimated_delivery_date)) > 100;
""",
    conn,
)
df.to_csv("exports/investigation/outlier_late_deliveries.csv", index=False)

df2 = pd.read_sql_query(
    """
SELECT 
    o.order_id,
    o.order_purchase_timestamp,
    o.order_approved_at,
    o.order_delivered_carrier_date,
    o.order_delivered_customer_date,
    o.order_estimated_delivery_date,
    JULIANDAY(o.order_delivered_customer_date) - JULIANDAY(o.order_estimated_delivery_date) AS delivery_delay_days
FROM olist_orders_dataset o
WHERE o.order_status = 'delivered'
AND (JULIANDAY(o.order_delivered_customer_date) - JULIANDAY(o.order_estimated_delivery_date)) < -100;
""",
    conn,
)
df2.to_csv("exports/investigation/outlier_early_deliveries.csv", index=False)
